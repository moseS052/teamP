package com.team.pj;

public class noteVO {
	private int n_no;
	private String n_con;
	private String n_date;
	private int m_no;
	private int m_pa_no;
	
	public noteVO() {
	}

	public int getN_no() {
		return n_no;
	}

	public void setN_no(int n_no) {
		this.n_no = n_no;
	}

	public String getN_con() {
		return n_con;
	}

	public void setN_con(String n_con) {
		this.n_con = n_con;
	}

	public String getN_date() {
		return n_date;
	}

	public void setN_date(String n_date) {
		this.n_date = n_date;
	}

	public int getM_no() {
		return m_no;
	}

	public void setM_no(int m_no) {
		this.m_no = m_no;
	}

	public int getM_pa_no() {
		return m_pa_no;
	}

	public void setM_pa_no(int m_pa_no) {
		this.m_pa_no = m_pa_no;
	}
	
	
}
