package com.team.pj;

public class alarmVO {
	private int m_no;
	private String alarm;
	private String al_time;
	private int al_check;
	
	public alarmVO() {
	}

	public int getM_no() {
		return m_no;
	}

	public void setM_no(int m_no) {
		this.m_no = m_no;
	}

	public String getAlarm() {
		return alarm;
	}

	public void setAlarm(String alarm) {
		this.alarm = alarm;
	}

	public String getAl_time() {
		return al_time;
	}

	public void setAl_time(String al_time) {
		this.al_time = al_time;
	}

	public int getAl_check() {
		return al_check;
	}

	public void setAl_check(int al_check) {
		this.al_check = al_check;
	}
	
}
